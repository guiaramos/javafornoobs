public class StringConcatenation {

    public static void main(String[] args) {

        //Strings
        String firstName;
        String lastName;
        String fullName;

        //Criterias
        firstName = "Guilherme";
        lastName = "Ramos";
        fullName = firstName + " " + lastName;

        //Print out
        System.out.println(fullName);

    }
}
