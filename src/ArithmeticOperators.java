public class ArithmeticOperators {

    public static void main(String[] args) {
        //variables
        int x;
        int y;

        //values
        x = 10;
        y = 5;

        //results
        System.out.println(x + y);
        System.out.println(x - y);
        System.out.println(x / y);
        System.out.println(x * y);
        System.out.println(x % y);
    }
}
